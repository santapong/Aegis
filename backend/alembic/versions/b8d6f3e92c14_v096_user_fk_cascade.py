"""v0.9.6 add ON DELETE CASCADE to user-owned FKs

Revision ID: b8d6f3e92c14
Revises: a7c5e2f81d20
Create Date: 2026-05-16 19:30:00.000000

Background: until v0.9.6, no foreign key declared an ON DELETE action,
so Postgres / MySQL defaulted to NO ACTION (RESTRICT). The practical
effect: deleting a User raised IntegrityError because every child row
blocked it. Account deactivation (`is_active=False`) was the only
safe path.

This migration adds ON DELETE CASCADE to every user-owned FK across
all 12 child tables. After this, deleting a User row deletes their
transactions, plans, budgets, tags, debts, payments, notifications,
trips, investments, savings goals, AI recommendations, and
preferences atomically.

GDPR-relevant: this is the schema underpinning of an eventual
"delete my account" endpoint. The endpoint itself isn't added in
this migration — flipping `is_active=False` is still the recommended
soft-delete path — but the schema is now ready.

Compatibility:
- PostgreSQL: native ALTER CONSTRAINT replacement via batch.
- MySQL/MariaDB: same DDL, FK name auto-generated by InnoDB.
- SQLite: requires CREATE-rename-COPY-DROP dance via batch_alter_table.

FK name discovery: we use SQLAlchemy's Inspector to look up the real
constraint name at migration time rather than guessing. Earlier
versions of this migration tried 3 name patterns inside a try/except,
which on Postgres can poison the surrounding transaction when one of
the guesses fails — the next op in the same batch then fails too.
Inspector-based lookup avoids that whole class of bug.
"""
