depends_on: Union[str, Sequence[str], None] = None


# (table, column, target_table) for every user-owned FK in the schema.
_USER_FK_TABLES: list[tuple[str, str, str]] = [
    ("transactions", "user_id", "users"),
    ("plans", "user_id", "users"),
    ("budgets", "user_id", "users"),
    ("payments", "user_id", "users"),
    ("notifications", "user_id", "users"),
    ("trips", "user_id", "users"),
    ("investments", "user_id", "users"),
    ("user_preferences", "user_id", "users"),
    ("savings_goals", "user_id", "users"),
    ("debts", "user_id", "users"),
    ("ai_recommendations", "user_id", "users"),
    ("tags", "user_id", "users"),
]

# Canonical name we give the new FK on every dialect. Lets downgrade
# find it again without inspector calls.
def _new_fk_name(table: str, column: str) -> str:
    return f"{table}_{column}_fkey"


def _existing_fk_name(table: str, column: str) -> str | None:
    """Look up the real constraint name for ``(table, column) →
    users.id`` via the live introspector. Returns None if no FK matches,
    which on a populated DB means the schema is unexpected — we'd rather
    fail loudly than silently create a duplicate."""
    bind = op.get_bind()
    insp = inspect(bind)
    for fk in insp.get_foreign_keys(table):
        if fk.get("referred_table") == "users" and column in (fk.get("constrained_columns") or []):
            return fk.get("name")
    return None


def upgrade() -> None:
    for table, column, target in _USER_FK_TABLES:
        existing = _existing_fk_name(table, column)
        with op.batch_alter_table(table) as batch:
            if existing:
                batch.drop_constraint(existing, type_="foreignkey")
            else:
                # Fresh DB / schema reset: there is no existing FK to
                # drop. Skip the drop and create the new one directly.
                pass
            batch.create_foreign_key(
                _new_fk_name(table, column),
                target,
                [column],
                ["id"],
                ondelete="CASCADE",
            )


def downgrade() -> None:
    """Re-create the FK without ondelete to restore NO ACTION semantics.

    Looks up the existing constraint by inspector — by this point it
    should always be the canonical name we set in upgrade(), but a
    direct DB tweak could have renamed it.
    """
    for table, column, target in _USER_FK_TABLES:
        existing = _existing_fk_name(table, column) or _new_fk_name(table, column)
        with op.batch_alter_table(table) as batch:
            batch.drop_constraint(existing, type_="foreignkey")
            batch.create_foreign_key(
                _new_fk_name(table, column),
                target,
                [column],
                ["id"],
            )
